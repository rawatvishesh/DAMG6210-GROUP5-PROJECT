CREATE SEQUENCE AUTOGENERATED_ID START WITH 1;
CREATE OR REPLACE TRIGGER TRIGGER_Customer BEFORE INSERT ON Customers FOR EACH ROW BEGIN SELECT AUTOGENERATED_ID.NEXTVAL INTO : NEW.Customer_id FROM DUAL; 
End;
/



